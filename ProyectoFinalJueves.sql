        total_pedidos := total_pedidos + 1;
    END LOOP; 
    
    CLOSE c_productos;

    IF total_pedidos > 0 THEN
        promedio := total_productos / total_pedidos;
        RETURN FLOOR(promedio);
    ELSE
        RETURN 0;
    END IF;
END;

